        private byte[] WriteTarget(uint target, byte[] data, uint source = 0)
        {
            Buffer.BlockCopy(GetTimeStampHex(), 0, data, 0x08, 0x04);

            if (source == 0)
                source = target;

            SubPacket sp = new SubPacket
            {
                SourceId = source,
                TargetId = target,
                Data = data
            };

            Packet packet = new Packet(sp);

            return packet.ToBytes();
        }

        public static int GetTimeStamp() => (int)(DateTime.UtcNow - new DateTime(1970, 1, 1, 0, 0, 0, DateTimeKind.Utc)).TotalSeconds;

        public static byte[] GetTimeStampHex() => BitConverter.GetBytes(GetTimeStamp());
