        private byte[] WriteTarget(uint id, byte[] data)
        {
            Buffer.BlockCopy(BitConverter.GetBytes(id), 0, data, 0x14, 0x04);
            Buffer.BlockCopy(BitConverter.GetBytes(id), 0, data, 0x18, 0x04);
            Buffer.BlockCopy(GetTimeStampHex(), 0, data, 0x28, 0x04);

            return data;
        }

