        {
            byte[] toBytes = new byte[Items.Count];
            return toBytes;
        }

        public void Add(Item item) => Items.Add(item);

        public void ChunkStartPacket(Socket sender, MaxSize maxSize, Type type)
        {
            byte[] data = new byte[0x08];
            Buffer.BlockCopy(BitConverter.GetBytes(ActorId), 0, data, 0, sizeof(uint));
            Buffer.BlockCopy(BitConverter.GetBytes((ushort)maxSize), 0, data, 0x04, sizeof(ushort));
            Buffer.BlockCopy(BitConverter.GetBytes((ushort)type), 0, data, 0x06, sizeof(ushort));
           

            SendPacket(sender, Opcodes.ChunkStart, data);
        }

        public void ChunkEndPacket(Socket sender) => SendPacket(sender, Opcodes.ChunkEnd, new byte[0x08]);       
        public void InventoryStartPacket(Socket sender) => SendPacket(sender, Opcodes.InventoryStart, new byte[0x08]);       
        public void InventoryEndPacket(Socket sender) => SendPacket(sender, Opcodes.InventoryEnd, new byte[0x08]);     
        public void ChunkPacket(Socket sender, Opcodes opcode, byte[] data) => SendPacket(sender, opcode, data);

