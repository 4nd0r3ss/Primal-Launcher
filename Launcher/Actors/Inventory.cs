        private void SendItems(Socket sender, Dictionary<ushort, object> inventory)
        {
            //get the number if items in the inventory
            List<Item> items = new List<Item>();
            foreach (var slot in inventory)
                if (slot.Value != null)
                    items.Add((Item)slot.Value);

            //if there are items, process.
            if (items.Count > 0)
            {
                int chunkIndex = 0;
                byte[] chunkx08 = new byte[0x388];

                for(int i=0;i< items.Count; i++)
                {                
                    if(chunkIndex == 7)
                    {
                        chunkx08[0x380] = 8;
                        ChunkPacket(sender, Opcodes.x08Chunk, chunkx08);
                        chunkx08 = new byte[0x388];
                        chunkIndex = 0;
                    }

                    Buffer.BlockCopy(items[i].ToBytes(), 0, chunkx08, (0x70 * chunkIndex), 0x70);
                    chunkIndex++;
                }
                 
                //send remaining items
                if(chunkIndex > 0)
                {
                    chunkx08[0x380] = (byte)chunkIndex;
                    ChunkPacket(sender, Opcodes.x08Chunk, chunkx08);
                }               
            }
        }
