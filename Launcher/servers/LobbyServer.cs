        private void ModifyCharacter(Socket handler, SubPacket sp)
        {
            File.WriteAllBytes("characreation_in.txt", sp.Data);

            byte[] responseData = new byte[0x50];
            byte command = sp.Data[0x21];

            switch (command)
            {
                case 0x01:
                    /*
                     * As this is intended to be a single player experience, no name reserving is needed.
                     */
                    Buffer.BlockCopy(sp.Data, 0x24, _newCharacter.Name, 0, 0x20);
                    _newCharacter.Slot = sp.Data[0x20];
                                        
                    _newCharacter.WorldId = sp.Data[0x22];
                    byte[] worldName = Encoding.ASCII.GetBytes(_worldRepo.GetWorld(_newCharacter.WorldId).Name);                    

                    responseData[0x00] = sp.Data[0x10]; //sequence
                    responseData[0x08] = (byte)1; //unknown
                    responseData[0x09] = (byte)1; //unknown
                    responseData[0x0a] = command;

                    responseData[0x00] = sp.Data[0x10]; //pid
                    responseData[0x00] = sp.Data[0x10]; //cid
                    Buffer.BlockCopy(BitConverter.GetBytes(0x400017), 0, responseData, 0x18, 0x03); //type           
                    responseData[0x1c] = (byte)1; //ticket

                    Buffer.BlockCopy(_newCharacter.Name, 0, responseData, 0x20, 0x20);
                    Buffer.BlockCopy(worldName, 0, responseData, 0x40, worldName.Length);                    

                    _log.Message("Character name reserved.");
                    break;

                case 0x02:
                    _newCharacter.Setup(sp.Data);
                    _user.AccountList[0].CharacterList.Add(_newCharacter);
                    UserRepository.UpdateUser(_user);
                    _log.Success("Character \"" + Encoding.ASCII.GetString(_newCharacter.Name) + "\" created!");
                    break;

                case 0x03:
                    //rename
                    break;

                case 0x04:
                    //delete
                    break;

                case 0x05:
                    _log.Error("Unknown Modifycharacter() command: 0x05");
                    break;

                case 0x06:
                    //rename retainer
                    break;
            }

            File.WriteAllBytes("characreation_out.txt", responseData);

            GamePacket response = new GamePacket
            {
                Opcode = 0x0e,
                TimeStamp = GetTimeStampHex(),
                Data = responseData
            };

            Packet packet = new Packet(response);
            handler.Send(packet.Build(ref _blowfish));           
        }

